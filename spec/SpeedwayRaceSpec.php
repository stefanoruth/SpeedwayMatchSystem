<?php

namespace spec;

use PhpSpec\ObjectBehavior;
use Prophecy\Argument;

class SpeedwayRaceSpec extends ObjectBehavior
{
    function it_is_initializable()
    {
        $this->shouldHaveType('SpeedwayRace');
    }

    function its_is_4_drivers_race()
    {
    	$this->race(4)->shouldReturn([
			1 => [1,4,2,3],
			2 => [2,1,3,4],
			3 => [3,2,4,1],
			4 => [4,3,1,2],
		]);
    }

    function its_is_5_drivers_race()
    {
    	$this->race(5)->shouldReturn([
    		1 => [1,5,3,4],
			2 => [2,1,4,5],
			3 => [3,2,5,1],
			4 => [4,3,1,2],
			5 => [5,4,2,3],
    	]);
    }

    function its_is_6_drivers_race()
    {
    	$this->race(6)->shouldReturn([
    		1 => [1,6,4,2],
			2 => [2,1,5,3],
			3 => [3,2,6,4],
			4 => [4,3,1,5],
			5 => [5,4,2,6],
			6 => [6,5,3,1],
    	]);
    }

    function its_is_7_drivers_race()
    {
    	$this->race(7)->shouldReturn([
			1 => [1,7,5,2],
			2 => [2,1,6,3],
			3 => [3,2,7,4],
			4 => [4,3,1,5],
			5 => [5,4,2,6],
			6 => [6,5,3,7],
			7 => [7,6,4,1],
		]);
    }

    function its_is_8_drivers_race()
    {
    	$this->race(8)->shouldReturn([
			1 => [1,8,6,3],
			2 => [2,1,7,4],
			3 => [3,2,8,5],
			4 => [4,3,1,6],
			5 => [5,4,2,7],
			6 => [6,5,3,8],
			7 => [7,6,4,1],
			8 => [8,7,5,2],
		]);
    }

    function its_is_10_drivers_race()
    {
    	$this->race(10)->shouldReturn([
			1  => [1,10,8,5],
			2  => [2,1,9,6],
			3  => [3,2,10,7],
			4  => [4,3,1,8],
			5  => [5,4,2,9],
			6  => [6,5,3,10],
			7  => [7,6,4,1],
			8  => [8,7,5,2],
			9  => [9,8,6,3],
			10 => [10,9,7,4],
		]);
    }

    function its_is_16_drivers_race()
    {
    	$this->race(16)->shouldReturn([
			1  => [1,16,14,11],
			2  => [2,1,15,12],
			3  => [3,2,16,13],
			4  => [4,3,1,14],
			5  => [5,4,2,15],
			6  => [6,5,3,16],
			7  => [7,6,4,1],
			8  => [8,7,5,2],
			9  => [9,8,6,3],
			10 => [10,9,7,4],
			11 => [11,10,8,5],
			12 => [12,11,9,6],
			13 => [13,12,10,7],
			14 => [14,13,11,8],
			15 => [15,14,12,9],
			16 => [16,15,13,10],
    	]);
    }

    function its_is_20_drivers_race()
    {
    	$this->race(20)->shouldReturn([
			1  => [1,20,18,15],
			2  => [2,1,19,16],
			3  => [3,2,20,17],
			4  => [4,3,1,18],
			5  => [5,4,2,19],
			6  => [6,5,3,20],
			7  => [7,6,4,1],
			8  => [8,7,5,2],
			9  => [9,8,6,3],
			10 => [10,9,7,4],
			11 => [11,10,8,5],
			12 => [12,11,9,6],
			13 => [13,12,10,7],
			14 => [14,13,11,8],
			15 => [15,14,12,9],
			16 => [16,15,13,10],
			17 => [17,16,14,11],
			18 => [18,17,15,12],
			19 => [19,18,16,13],
			20 => [20,19,17,14],
    	]);
    }
}
